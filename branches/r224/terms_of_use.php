<h1>Podmienky využívania služby Fajr</h1>

<p>
<ol>
<li> Súhlas s podmienkami:
		<ol>
				<li>Používanie: Aby ste mohli službu používať, musíte najskôr vyjadriť svoj
						súhlas s podmienkami. Ak s podmienkami nesúhlasíte, nemôžete službu využívať.
				</li>
				<li>Súhlas s podmienkami vyjadrujete tým, že kliknete na tlačidlo "prihlásiť"
				</li>
		</ol>
</li>
<li> Poskytovanie služby:
		<ol> 
				<li>Autori službu priebežne inovujú, aby svojim používateľom mohla
						vždy ponúknuť čo možno najväčší úžitok. Ste si preto vedomí toho a
						súhlasíte s tým, že podoba a povaha služieb, ktoré Vám budú
						poskytované sa môže kedykoľvek zmeniť bez toho, že by ste o tom
						boli vopred informovaní.
				</li>
				<li>V rámci týchto priebežných inovácií beriete na vedomie a
						súhlasíte s tým, že služba (alebo niektoré jej funkcie) môže
						prestať fungovat, a to výhradne podľa vlastného uváženia a bez
						povinnosti Vás o tom vopred informovať. Aj Vy môžete kedykoľvek
						prestať využívať službu bez toho, že by ste ju o tom museli zvlášť
						informovať.
				</li>
		</ol>
</li>
<li> Používanie služby:
		<ol>
				<li>Pre prístup k niektorým službám môže byť nevyhnutné poskytnutie Vašich údajov
						(napr. prihlasovacieho mena a hesla do univerzitného systému).
				</li>
				<li>Zaväzujete sa, že budete služby využívať iba pre účely, ktoré
						sú povolené (a) v podmienkach a (b) v platných právnych predpisoch
						a všeobecne prijímaných postupoch či zásadách v danom právnom
						poriadku.
				</li>
				<li>Zaväzujete sa, že nebudete činiť nič, čo by narušovalo alebo
						poškodzovalo službu (alebo servery a siete, ktoré sú pripojené k
						službe).
				</li>
		</ol>
</li>
<li>Vaše heslá a zabezpečenie Vášho používateľského účtu:
		<ol>
				<li>Beriete na vedomie a súhlasíte s tým, že za utajenie hesiel k
						všetkým používateľským účtom, ktoré budete používať pre prístup k službám,
						zodpovedáte Vy.
				</li>
		</ol>
</li>


<li>Vylúčenie záruky:
		<ol>
				<li>Služba je poskytované tak, ako je a prevádzkovateľ služby Vám
						v súvislosti so službou neposkytuje žiadnu záruku.
				</li>
				<li>Predovšetkým, Služba nezodpovedá za to, že:
						<ul><li>používanie služby bude spĺňať Vaše požiadavky,
								</li>
								<li>budete môcť službu používať nepretržite, včas,
										bezpečne a bez chýb,
								</li>
								<li>informácie, ktoré získate ako výsledok používania
										služby, budú presné alebo spoľahlivé, a
								</li>
								<li>vady v prevádzke alebo funkčnosti softvéru, ktorý Vám
										bude poskytnutý ako súčasť služieb, budú odstránené.
								</li>
						</ul>
				</li>
		</ol>
</li>

<li>Ak to nie je výslovne uvedené v týchto podmienkach, tak sa na
		službu nevzťahujú žiadne ďalšie podmienky a záruky vrátane predpokladaných
		podmienok a záruk za dostatočnú kvalitu, vhodnosť na určitý účel alebo
		súlad s popisom.
</li>

<li>Prevádzkovateľ služby nenesie zodpovednosť za:
		<ol>
				<li>nepriame alebo následné škody, ktoré Vám prípadne vzniknú
				</li>
				<li>straty a škody, ktoré Vám prípadne vzniknú:
						<ul>
								<li>v dôsledku zmien, ktoré sa na službe vykonajú, alebo
										trvalého či dočasného prerušenia poskytovania služby (alebo
										niektorých jej vlastností),
								</li>
								<li>vymazaním, poškodením alebo neuložením obsahu či
										akýchkoľvek iných prenášaných dát spravovaných či
										prenášaných službami alebo ich prostredníctvom,
								</li>
								<li>ak dôjde k prezradeniu Vášho hesla alebo iných
										prístupových údajov k Vášmu používateľskému účtu.
								</li>
						</ul>
				</li>
		</ol>
</li>
</ol>
